/*****************************************************************************/
void	CCore::LayerAdd(char *Name)
{
sLayer	NewLayer;

	if (!Name)
	{
		char	DynName[32];
		sprintf(DynName,"Layer%2d",Layers.size()+1);
		Name=DynName;
	}

	strcpy(NewLayer.Name,Name);
	TRACE1("New Layer [%s]\n",Name);
	Layers.push_back(NewLayer);

}

